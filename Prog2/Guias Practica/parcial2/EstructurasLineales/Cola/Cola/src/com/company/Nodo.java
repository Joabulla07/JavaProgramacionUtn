package com.company;

public class Nodo {

    private int valor;
    private Nodo next;


    public Nodo() {
    }

    public Nodo(int data) {
        this.valor = valor;
    }


    public int getValor() {
        return valor;
    }

    public void setValor(int data) {
        this.valor = data;
    }

    public Nodo getNext() {
        return next;
    }

    public void setNext(Nodo next) {
        this.next = next;
    }

}